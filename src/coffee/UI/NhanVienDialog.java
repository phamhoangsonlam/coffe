    void Last() {
        this.row = tblNhanVien.getRowCount() - 1;
        this.edit();
    }

    
    void Next() {
        if (this.row < tblNhanVien.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    
    void First() {
        this.row = 0;
        this.edit();
    }
    

    void Prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }
    

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblNhanVien.getRowCount() - 1);
        txtMaNV.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    
    void clearForm() {
        NhanVien nv = new NhanVien();
        this.setForm1(nv);
        txtSoNgayLamViec.setText("");
        txtTienThuongThem.setText("");
        txtTongTienLuong.setText("");
        this.row = -1;
        this.updateStatus();
    }
    
    
    void tinhTien() {
        while (true) {
            if (txtSoNgayLamViec.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Số ngày làm việc không được bỏ trống ! ");
                return;
            } else if (!txtSoNgayLamViec.getText().trim().matches("[0-9]+")) {
                JOptionPane.showMessageDialog(null, "Ngày làm việc phải là số ! ");
                return;
            } else if (Double.parseDouble(txtSoNgayLamViec.getText().trim()) > 31) {
                JOptionPane.showMessageDialog(null, "Số ngày làm việc phải nhỏ hơn 31");
                return;
            } else {
                break;
            }
        }
        double ngay = Double.parseDouble(txtSoNgayLamViec.getText().trim());
        String a = (String) cboTienLuong.getSelectedItem();
        double LuongCB = Double.parseDouble(a.replaceAll(",", ""));
        double TienLuong;

        if (txtTienThuongThem.getText().trim().equals("")) {
            TienLuong = ngay * LuongCB;
        } else {
            while (true) {
                if (!txtTienThuongThem.getText().trim().matches("[0-9]+")) {
                    JOptionPane.showMessageDialog(null, "Tiền thưởng phải là số ! ");
                    return;
                } else if (Double.parseDouble(txtTienThuongThem.getText().trim()) > 500000) {
                    JOptionPane.showMessageDialog(null, "Tiền thưởng phải nhỏ hơn 500.000 ");
                    return;
                } else {
                    break;
                }
            }
            double Thuong = Double.parseDouble(txtTienThuongThem.getText().trim());
            TienLuong = (ngay * LuongCB) + Thuong;
        }
        txtTongTienLuong.setText(formatter.format(TienLuong) + " VNĐ");
    }
    
    
