    void clearForm1() {
        txtMaSP.setText("");
        txtTenSP.setText("");
        cboSize.setSelectedIndex(0);
        txtDonGia.setText("");
        this.row1 = -1;
        this.clearImg();
        this.updateStatus1();
    }

    void insert1() {
        SanPham sp = getForm1();
        try {
            dao1.insert(sp);
            this.fillTable1();
            this.clearForm1();
            MsgBox.alert(this, "Thêm sản phẩm thành công");
        } catch (Exception e) {
//                System.out.println(e);
            MsgBox.alert(this, "Thêm sản phẩm thất bại");
        }
    }

    void update1() {
        SanPham sp = getForm1();
        try {
            dao1.update(sp);
            this.fillTable1();
            this.clearForm1();
            MsgBox.alert(this, "Cập nhật thành công");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại");
        }
    }

    void delete1() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Bạn không có quyền xóa sản phẩm!");
        } else {
            if (MsgBox.confirm(this, "Bạn thực sự muốn xóa sản phẩm này!")) {
                String masp = txtMaSP.getText();
                try {
                    dao1.delete(masp);
                    this.fillTable1();
                    this.clearForm1();
                    MsgBox.alert(this, "Xóa thành công");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại");
                }
            }
        }
    }

