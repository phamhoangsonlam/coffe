    void chonAnh() {
        fileChooser = new JFileChooser("logos\\");
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            XImage.save(file);// lưu hình vào thư mục logos
//            ImageIcon icon = XImage.read(file.getName());// đọc hình từ logos

            ImageIcon image = new ImageIcon("" + XImage.read(file.getName()));
            Image im = image.getImage();
            ImageIcon icon = new ImageIcon(im.getScaledInstance(lblAnh.getWidth(),
                    lblAnh.getHeight(), im.SCALE_SMOOTH));
            lblAnh.setIcon(icon);

//            lblHinh.setIcon(icon);
            lblAnh.setToolTipText(file.getName());// giữ tên hình trong tooltip
        }
    }

    void fillComboBoxLoaiSanPham() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboLoaiSP.getModel();
        model.removeAllElements();
        List<LoaiSanPham> list = dao2.selectAll();
        for (LoaiSanPham lsp : list) {
            model.addElement(lsp);
        }
    }

    void fillTable1() {
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);
        LoaiSanPham lsp = (LoaiSanPham) cboLoaiSP.getSelectedItem();
        // Hiện thị all sp khi chọn check box
        if (cboHienThiSP.isSelected()) {
            List<SanPham> list = dao1.selectAll();
            for (SanPham sp : list) {
                String tenlsp = dao2.selectById(sp.getLoaiSP()).getTenLoaiSP();
                model.addRow(new Object[]{
                    sp.getMaSP(), sp.getTenSP(), tenlsp, sp.getSize(),
                    formatter.format(sp.getDonGia()), sp.getHinh()
                });
            }
        } else if (lsp != null) {
            List<SanPham> list = dao1.selectByLoaiSP(lsp.getMaLoaiSP());
            for (SanPham sp : list) {
                String tenlsp = dao2.selectById(sp.getLoaiSP()).getTenLoaiSP();
                model.addRow(new Object[]{
                    sp.getMaSP(), sp.getTenSP(), tenlsp, sp.getSize(),
                    formatter.format(sp.getDonGia()), sp.getHinh()
                });
            }
        }
    }

